"""Type hints."""


def function_string() -> str:
    """Return a string."""
    return "Hello there!"


def function_int() -> int:
    """Return an integer."""
    return 32


def function_float() -> float:
    """Return a float."""
    return 32.9


def function_bool() -> bool:
    """Return a boolean value."""
    return True


def function_none() -> None:
    """Return nothing (None)."""
    print("You dont see it !")
